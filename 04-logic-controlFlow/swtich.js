const month = "april"
switch (month) {
    case "jan":
        console.log("January")
        break;
    case "feb":
        console.log("Feb")
        break;
    case "mar":
        console.log("March")
        break;
    case "april":
        console.log("April")
        break;

    default:
        console.log("Wrong case match")
        break;
}